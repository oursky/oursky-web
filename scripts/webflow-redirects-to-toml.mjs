#!/usr/bin/env node
/**
 * Convert a Webflow redirects CSV (Site Settings → Hosting → Redirects → Export)
 * into Netlify-style `[[redirects]]` blocks ready to paste into `netlify.toml`.
 *
 * Usage:
 *   node scripts/webflow-redirects-to-toml.mjs <input.csv> [--out netlify-redirects.toml]
 *   node scripts/webflow-redirects-to-toml.mjs <input.csv> --stdout
 *
 * Behaviour:
 *   - Auto-detects column names (Old Path / Source / From; New Path / Target / To).
 *   - Emits a `.html` extension catch-all on top.
 *   - Adds `force = true` to any rule whose source path collides with a route
 *     this site already generates (top-level page, blog slug, works slug, or
 *     category slug). Webflow source paths starting with `/blogs/<slug>` or
 *     `/works/<slug>` are checked against actual content collections.
 *   - Skips rules that 1:1 map a path to itself (Webflow exports include these).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

const args = process.argv.slice(2);
if (args.length === 0 || args.includes('--help') || args.includes('-h')) {
  console.error('Usage: node scripts/webflow-redirects-to-toml.mjs <input.csv> [--out path | --stdout]');
  process.exit(1);
}
const inputPath = args[0];
const outIdx = args.indexOf('--out');
const outPath = outIdx >= 0 ? args[outIdx + 1] : path.join(ROOT, 'netlify-redirects.toml');
const toStdout = args.includes('--stdout');

// ── Parse CSV ────────────────────────────────────────────────────────────────
function parseCsv(text) {
  const rows = [];
  let cur = [''];
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"' && text[i + 1] === '"') { cur[cur.length - 1] += '"'; i++; }
      else if (c === '"') inQuotes = false;
      else cur[cur.length - 1] += c;
    } else {
      if (c === '"') inQuotes = true;
      else if (c === ',') cur.push('');
      else if (c === '\r') {} // ignore
      else if (c === '\n') { rows.push(cur); cur = ['']; }
      else cur[cur.length - 1] += c;
    }
  }
  if (cur.length > 1 || cur[0] !== '') rows.push(cur);
  return rows;
}

const raw = fs.readFileSync(inputPath, 'utf8');
const rows = parseCsv(raw).filter((r) => r.some((cell) => cell.trim().length > 0));
if (rows.length < 2) {
  console.error('CSV has no data rows.');
  process.exit(1);
}
const header = rows[0].map((h) => h.trim().toLowerCase());
const findCol = (...candidates) => {
  for (const cand of candidates) {
    const idx = header.findIndex((h) => h === cand);
    if (idx >= 0) return idx;
  }
  return -1;
};
const fromIdx = findCol('old path', 'source', 'from', 'source path');
const toIdx = findCol('new path', 'target', 'to', 'destination', 'target path');
if (fromIdx < 0 || toIdx < 0) {
  console.error(`Could not detect from/to columns. Header was: ${header.join(', ')}`);
  process.exit(1);
}

// ── Discover routes that already exist (for force=true detection) ────────────
const existingRoutes = new Set();
existingRoutes.add('/');

function walkPages(dir, prefix = '') {
  if (!fs.existsSync(dir)) return;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      walkPages(path.join(dir, entry.name), `${prefix}/${entry.name}`);
      continue;
    }
    if (!/\.(astro|md|mdx)$/.test(entry.name)) continue;
    const base = entry.name.replace(/\.(astro|md|mdx)$/, '');
    if (base.startsWith('[') || base === 'index') {
      if (base === 'index' && prefix) existingRoutes.add(prefix);
      continue;
    }
    existingRoutes.add(`${prefix}/${base}`);
  }
}
walkPages(path.join(ROOT, 'src/pages'));

const collectFiles = (dir, ext) =>
  fs.existsSync(dir)
    ? fs.readdirSync(dir).filter((f) => f.endsWith(ext)).map((f) => f.replace(ext, ''))
    : [];
for (const slug of collectFiles(path.join(ROOT, 'src/content/blog'), '.md')) existingRoutes.add(`/blogs/${slug}`);
for (const slug of collectFiles(path.join(ROOT, 'src/content/blog'), '.mdx')) existingRoutes.add(`/blogs/${slug}`);
for (const slug of collectFiles(path.join(ROOT, 'src/content/works'), '.md')) existingRoutes.add(`/works/${slug}`);
for (const slug of collectFiles(path.join(ROOT, 'src/content/works'), '.mdx')) existingRoutes.add(`/works/${slug}`);
for (const slug of collectFiles(path.join(ROOT, 'src/content/categories'), '.json')) existingRoutes.add(`/blog-category/${slug}`);

// ── Build redirect rules ─────────────────────────────────────────────────────
const lines = [];
lines.push('# Generated by scripts/webflow-redirects-to-toml.mjs');
lines.push(`# Source: ${path.relative(ROOT, path.resolve(inputPath))}`);
lines.push(`# Generated: ${new Date().toISOString()}`);
lines.push('');
lines.push('# ── .html extension stripping (Webflow legacy URLs) ─────────────');
lines.push('[[redirects]]');
lines.push('  from   = "/*.html"');
lines.push('  to     = "/:splat"');
lines.push('  status = 301');
lines.push('');
lines.push('# ── Imported from Webflow CSV ───────────────────────────────────');
lines.push('');

const normalize = (p) => {
  if (!p) return '';
  let s = p.trim();
  if (!s.startsWith('/') && !/^https?:\/\//.test(s)) s = `/${s}`;
  return s.replace(/\/$/, '') || '/';
};

let kept = 0;
let skipped = 0;
let forced = 0;

for (let i = 1; i < rows.length; i++) {
  const row = rows[i];
  const from = normalize(row[fromIdx] || '');
  const to = normalize(row[toIdx] || '');
  if (!from || !to) { skipped++; continue; }
  if (from === to) { skipped++; continue; }

  // If the source URL is also a real route on the new site, Netlify will serve
  // the static file unless we force the redirect.
  const sourceProbe = from.replace(/\/\*$/, '');
  const needsForce = existingRoutes.has(sourceProbe);

  lines.push('[[redirects]]');
  lines.push(`  from   = "${from}"`);
  lines.push(`  to     = "${to}"`);
  lines.push('  status = 301');
  if (needsForce) {
    lines.push('  force  = true');
    forced++;
  }
  lines.push('');
  kept++;
}

const output = lines.join('\n');

if (toStdout) {
  process.stdout.write(output);
} else {
  fs.writeFileSync(outPath, output);
  console.error(`✓ Wrote ${kept} rule(s) to ${path.relative(ROOT, outPath)}`);
  console.error(`  ${forced} flagged with force = true (source collides with a generated route)`);
  console.error(`  ${skipped} row(s) skipped (empty or self-referential)`);
  console.error(`  + 1 baseline rule for /*.html stripping`);
  console.error('');
  console.error('Next: review the file, then merge its [[redirects]] blocks into netlify.toml.');
}
